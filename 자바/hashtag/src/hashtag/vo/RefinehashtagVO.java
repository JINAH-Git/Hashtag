//해시태그정제 데이터의 자료를 담기위한 VO(Value Object)클래스
package hashtag.vo;

public class  RefinehashtagVO 
{
	String rhtagno;         //블로그별해시태그일련번호        
	String rhrefine;        //정제차수 
	String rhno;      	    //게시글관리번호      
	String rhhashtag;		//해시태그명    
	String rhregiontag;     //지역해시태그명      
	String rhwdate;         //작성일
	String yearmonth;       //통합날짜(년,월만 출력)
	String metionamount;    //언급량
	
	
	public String getRhtagno() 						 { return rhtagno;					}
	public String getRhrefine() 					 { return rhrefine;					}
	public String getRhno() 						 { return rhno;						}
	public String getRhhashtag() 					 { return rhhashtag;				}
	public String getRhregiontag() 					 { return rhregiontag;				}
	public String getRhwdate() 						 { return rhwdate;					}
	public String getYearmonth() 				     { return yearmonth;				}
	public String getMetionamount() 				 { return metionamount;				}
	
	
	public void setRhtagno(String rhtagno) 			 { this.rhtagno     = rhtagno;		 }
	public void setRhrefine(String rhrefine) 		 { this.rhrefine    = rhrefine;		 }
	public void setRhno(String rhno) 				 { this.rhno        = rhno;			 }
	public void setRhhashtag(String rhhashtag) 		 { this.rhhashtag   = rhhashtag;	 }
	public void setRhregiontag(String rhregiontag) 	 { this.rhregiontag = rhregiontag;	 }
	public void setRhwdate(String rhwdate) 			 { this.rhwdate     = rhwdate;	  	 }
	public void setYearmonth(String yearmonth) 		 { this.yearmonth   = yearmonth;	 } 
	public void setMetionamount(String metionamount) { this.metionamount = metionamount; }
	
	
	
	
	
}
