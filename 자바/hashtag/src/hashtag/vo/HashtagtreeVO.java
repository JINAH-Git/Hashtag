//해시태그 트리 데이터의 자료를 담기위한 VO(Value Object)클래스
package hashtag.vo;

public class HashtagtreeVO 
{
	String htno;		//트리관리번호               
	String htregiontag;	//지역해시태그명             
	String hashtag1;	//해시태그1                   
	String hashtag2;	//해시태그2                    
	String htweight;	//가중치
	
	public String getHtno() 						{ return htno;					  }
	public String getHtregiontag() 					{ return htregiontag;			  }
	public String getHashtag1() 					{ return hashtag1;				  }
	public String getHashtag2() 					{ return hashtag2;				  }
	public String getHtweight() 					{ return htweight;				  }
	public void setHtno(String htno) 				{ this.htno 	   = htno;		  }
	public void setHtregiontag(String htregiontag) 	{ this.htregiontag = htregiontag; }
	public void setHashtag1(String hashtag1) 		{ this.hashtag1    = hashtag1;	  }
	public void setHashtag2(String hashtag2) 		{ this.hashtag2    = hashtag2;	  }
	public void setHtweight(String htweight) 		{ this.htweight    = htweight;	  }

	
	
}
